    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        String role = tb_pengguna.getRole();
        int row = jTable1.getSelectedRow();

        String Id = jTable1.getValueAt(row, 0).toString();
        String status = jTable1.getValueAt(row, 3).toString();

        if (role.equals("Superadmin")) {

            new DetailForm(Id, status).setVisible(true);

            jDateChooser1.setDate(null);
            jDateChooser2.setDate(null);
            jComboBox1.setSelectedIndex(0);

        } else if (role.equals("Admin") && status.equals("Selesai")) {

            new DetailForm(Id, status).setVisible(true);

            jDateChooser1.setDate(null);
            jDateChooser2.setDate(null);
            jComboBox1.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "Tidak memiliki akses !!");
        }
    }//GEN-LAST:event_jTable1MouseClicked

