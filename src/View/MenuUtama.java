        String awal = sdf.format(jDateChooser1.getDate());
        String akhir = sdf.format(jDateChooser2.getDate());

        if (awal.equals(null) || akhir.equals(null)) {
            JOptionPane.showMessageDialog(null, "Tanggal tidak boleh kosong !!");
        } else if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih status !!");
        } else {
            res = null;
            res = bkd.ShowKeluarByDate(jComboBox1.getSelectedItem().toString(), awal, akhir);
            tbm.SetTabel(jTable1, res, namaKolom, jmlKolom, lebar);
        }


